// 1. Определение текущего размера окна браузера:
function displayWindowSize() {
    const width = window.innerWidth;
    const height = window.innerHeight;
    console.log(`Ширина окна: ${width}, Высота окна: ${height}`);
}

window.addEventListener('resize', displayWindowSize);
//Этот код будет выводить текущую ширину и высоту окна браузера при его изменении.

// 2. Подтверждение закрытия страницы:
window.addEventListener('beforeunload', function(event) {
    event.preventDefault();
    event.returnValue = '';
    return '';
});
// Этот код создаст всплывающее окно, когда пользователь попытается закрыть вкладку браузера.

// 3. Управление историей переходов:
document.getElementById('backButton').addEventListener('click', function() {
    window.history.back();
});

document.getElementById('forwardButton').addEventListener('click', function() {
    window.history.forward();
});
